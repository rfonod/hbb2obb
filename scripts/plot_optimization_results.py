"""
HBB2OBB Benchmark Results Visualization Tool

This script creates visualization plots from benchmark results generated by optimize_hbb2obb.py.

Usage:
    python plot_optimization_results.py <benchmark_dir> [options]

Required Arguments:
    benchmark_dir    Path to benchmark results directory containing results.yaml

Optional Arguments:
    --output, -o     Path to save the output plot (default: benchmark_dir/plot.png)
    --dpi, -d        DPI for saved plot (default: 300)
    --show, -s       Display the plot instead of saving
    --no_time, -nt   Don't visualize execution time in the plot
    --title, -t      Custom plot title

Examples:
    # Basic usage - save plot to default location
    python scripts/plot_optimization_results.py data/benchmark_results/sam_b

    # Show plot without saving
    python scripts/plot_optimization_results.py data/benchmark_results/sam_b --show

    # Save with custom settings
    python scripts/plot_optimization_results.py data/benchmark_results/sam_b -o custom_plot.png --dpi 600
"""

import argparse
import os
from pathlib import Path

import matplotlib.pyplot as plt
import numpy as np
import yaml
from matplotlib.lines import Line2D


def parse_args():
    """Parse command line arguments."""
    parser = argparse.ArgumentParser(description="Visualize HBB2OBB benchmark results")

    # Required arguments
    parser.add_argument("benchmark_dir", type=Path, help="Path to benchmark results directory")

    # Optional arguments
    parser.add_argument("--output", "-o", type=Path, help="Path to save the output plot")
    parser.add_argument("--dpi", "-d", type=int, default=300, help="DPI for saved plot")
    parser.add_argument("--show", "-s", action="store_true", help="Display the plot instead of saving")
    parser.add_argument("--no_time", "-nt", action="store_true", help="Don't visualize execution time")
    parser.add_argument("--title", "-t", type=str, help="Custom plot title")

    return parser.parse_args()


def load_results(benchmark_dir):
    """Load results from YAML file."""
    results_path = benchmark_dir / "results.yaml"

    if not results_path.exists():
        raise FileNotFoundError(f"Results file not found: {results_path}")

    with open(results_path, 'r') as f:
        data = yaml.safe_load(f)

    return data


def organize_data_by_imgsz(results):
    """Organize benchmark results by image size."""
    data_by_imgsz = {}

    for result in results:
        imgsz = result['imgsz']
        if imgsz not in data_by_imgsz:
            data_by_imgsz[imgsz] = {'scale_factors': [], 'avg_iou': [], 'std_iou': [], 'execution_time': []}

        data_by_imgsz[imgsz]['scale_factors'].append(result['scale_factor'])
        data_by_imgsz[imgsz]['avg_iou'].append(result['avg_iou'])
        data_by_imgsz[imgsz]['std_iou'].append(result['std_iou'])
        data_by_imgsz[imgsz]['execution_time'].append(result['execution_time'])

    # Sort data points by scale factor
    for imgsz in data_by_imgsz:
        idx = np.argsort(data_by_imgsz[imgsz]['scale_factors'])
        data_by_imgsz[imgsz]['scale_factors'] = np.array(data_by_imgsz[imgsz]['scale_factors'])[idx]
        data_by_imgsz[imgsz]['avg_iou'] = np.array(data_by_imgsz[imgsz]['avg_iou'])[idx]
        data_by_imgsz[imgsz]['std_iou'] = np.array(data_by_imgsz[imgsz]['std_iou'])[idx]
        data_by_imgsz[imgsz]['execution_time'] = np.array(data_by_imgsz[imgsz]['execution_time'])[idx]

    return data_by_imgsz


def create_plot(data_by_imgsz, best_params, args):
    """Create a visualization plot of benchmark results."""

    plt.figure()

    # Colors and markers for different image sizes
    colors = ['#1f77b4', '#ff7f0e', '#2ca02c', '#d62728', '#9467bd', '#8c564b']
    markers = ['s', '^', 'o', 'D', 'P', '*', 'X']

    legend_elements = []

    # Determine marker size range based on execution time
    if not args.no_time:
        all_times = []
        for imgsz_data in data_by_imgsz.values():
            all_times.extend(imgsz_data['execution_time'])
        min_time, max_time = min(all_times), max(all_times)
        time_range = max_time - min_time

        # Marker size range (min and max size)
        min_size, max_size = 50, 150

    # Plot data for each image size
    for i, (imgsz, data) in enumerate(sorted(data_by_imgsz.items())):
        color = colors[i % len(colors)]
        marker = markers[i % len(markers)]

        # Calculate marker sizes based on execution time if requested
        if not args.no_time:
            marker_sizes = min_size + (data['execution_time'] - min_time) / time_range * (max_size - min_size)
        else:
            marker_sizes = [80] * len(data['scale_factors'])  # Fixed size

        # Plot the IoU values with error bars
        plt.errorbar(data['scale_factors'], data['avg_iou'], yerr=data['std_iou'], fmt='none', ecolor=color, alpha=0.3)

        plt.scatter(
            data['scale_factors'],
            data['avg_iou'],
            s=marker_sizes,
            color=color,
            marker=marker,
            alpha=0.7,
            edgecolors='black',
            linewidths=0.5,
            label=f"{imgsz}px",
        )

        # Add lines connecting points
        plt.plot(data['scale_factors'], data['avg_iou'], color=color, alpha=0.6, linestyle='-', linewidth=1.5)

        # Add to legend
        legend_elements.append(
            Line2D([0], [0], marker=marker, color='w', markerfacecolor=color, markersize=10, label=f"{imgsz}px")
        )

    # Mark the best parameters with a star
    if not args.no_time:
        # Find the execution time for the best parameters
        best_time = None
        for result in data_by_imgsz.get(best_params['imgsz'], {}).get('execution_time', []):
            best_time = result
            break

        if best_time is not None:
            best_marker_size = min_size + (best_time - min_time) / time_range * (max_size - min_size)
        else:
            best_marker_size = max_size  # Default to max size if can't find the time
    else:
        best_marker_size = 80  # Fixed size, same as other markers

    # Scale the marker up a bit to make it stand out, but keep it proportional
    best_marker_size *= 2.0

    plt.scatter(
        best_params['scale_factor'],
        best_params['avg_iou'],
        s=best_marker_size,
        color='gold',
        marker='*',
        edgecolors='black',
        linewidths=1.5,
        zorder=10,
        label="Best",
        alpha=0.7,
    )

    # Add text annotation for best parameters with adaptive placement
    x_best = best_params['scale_factor']
    y_best = best_params['avg_iou']

    # Collect points in each quadrant
    quadrant_points = {'top_right': [], 'top_left': [], 'bottom_right': [], 'bottom_left': []}

    for imgsz, data in data_by_imgsz.items():
        for sf, iou in zip(data['scale_factors'], data['avg_iou']):
            if sf == x_best and iou == y_best:
                continue  # Skip the best point itself

            # Determine which quadrant this point is in
            if sf >= x_best and iou >= y_best:
                quadrant_points['top_right'].append((sf, iou))
            elif sf < x_best and iou >= y_best:
                quadrant_points['top_left'].append((sf, iou))
            elif sf >= x_best and iou < y_best:
                quadrant_points['bottom_right'].append((sf, iou))
            else:
                quadrant_points['bottom_left'].append((sf, iou))

    # Find the quadrant with the fewest points
    min_quadrant = min(quadrant_points, key=lambda q: len(quadrant_points[q]))

    # Set annotation position based on the least crowded quadrant
    if min_quadrant == 'top_right':
        ha, va = 'left', 'bottom'
        xytext = (20, 20)
    elif min_quadrant == 'top_left':
        ha, va = 'right', 'bottom'
        xytext = (-20, 20)
    elif min_quadrant == 'bottom_right':
        ha, va = 'left', 'top'
        xytext = (20, -20)
    else:  # bottom_left
        ha, va = 'right', 'top'
        xytext = (-20, -20)

    plt.annotate(
        f"Best: {best_params['imgsz']}px, SF={best_params['scale_factor']:.3f}\nIoU={best_params['avg_iou']:.4f}±{best_params['std_iou']:.4f}",
        xy=(best_params['scale_factor'], best_params['avg_iou']),
        xytext=xytext,
        textcoords='offset points',
        bbox=dict(boxstyle='round,pad=0.5', fc='yellow', alpha=0.5),
        fontsize=9,
        ha=ha,
        va=va,
        arrowprops=dict(arrowstyle='->', lw=1.5, color='black', alpha=0.7),
    )

    # Add legend for image sizes
    legend1 = plt.legend(handles=legend_elements, loc='best', title="Image Size")
    plt.gca().add_artist(legend1)

    # Add marker size legend for execution time if requested
    if not args.no_time:
        # Create custom handles for marker size legend
        size_handles = []
        size_labels = []

        # Use 3 sizes for the legend
        for size_fraction, label_fraction in zip([0, 0.5, 1], [0, 0.5, 1]):
            size = min_size + size_fraction * (max_size - min_size)
            time = min_time + label_fraction * time_range
            size_handles.append(
                Line2D([0], [0], linestyle='none', marker='o', markersize=np.sqrt(size / 3), color='gray')
            )
            size_labels.append(f"{time:.1f}s")

        # Add execution time legend
        plt.legend(size_handles, size_labels, loc='lower left', title="Execution Time", framealpha=0.7)

    # Set plot title and labels
    if args.title:
        plt.title(args.title)
    else:
        config_path = args.benchmark_dir / "config.yaml"
        if config_path.exists():
            with open(config_path, 'r') as f:
                config = yaml.safe_load(f)
                run_name = config.get('run_name', os.path.basename(args.benchmark_dir))
                plt.title(f"HBB2OBB Benchmark Results: {run_name}")
        else:
            plt.title(f"HBB2OBB Benchmark Results: {os.path.basename(args.benchmark_dir)}")

    plt.xlabel("Scale Factor")
    plt.ylabel("Average IoU (with ±std)")
    plt.grid(True, linestyle='--', alpha=0.6)

    # Set decent axis limits
    plt.ylim(bottom=max(0, min([min(d['avg_iou']) for d in data_by_imgsz.values()]) - 0.05))

    # Add vertical line at scale_factor = 0
    plt.axvline(x=0, color='gray', linestyle='--', alpha=0.5)

    # Add plot annotations in the bottom right
    plt.text(
        0.98,
        0.02,
        f"Total GT: {best_params['total_gt']}, Matches: {best_params['total_matches']}",
        transform=plt.gca().transAxes,
        fontsize=8,
        alpha=0.7,
        bbox=dict(boxstyle='round,pad=0.3', fc='white', alpha=0.5),
        horizontalalignment='right',
        verticalalignment='bottom',
    )

    # Adjust layout
    plt.tight_layout()

    # Save or show the plot
    if args.show:
        plt.show()
    else:
        if args.output:
            output_path = args.output
        else:
            output_path = args.benchmark_dir / "plot.png"

        plt.savefig(output_path, dpi=args.dpi, bbox_inches='tight')
        print(f"Plot saved to: {output_path}")


def main():
    """Main function to load data and create plots."""
    args = parse_args()

    try:
        # Load results data
        data = load_results(args.benchmark_dir)

        # Organize data by image size
        results = data.get('all_results', [])
        best_params = data.get('best_parameters', {})

        # Check if data is valid
        if not results or not best_params:
            print("Error: Invalid benchmark results data structure")
            return

        data_by_imgsz = organize_data_by_imgsz(results)

        # Create and save/show the plot
        create_plot(data_by_imgsz, best_params, args)

    except Exception as e:
        print(f"Error: {e}")
        raise


if __name__ == "__main__":
    main()
